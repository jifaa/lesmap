-- 10. Create Helper Functions (SECURITY DEFINER to bypass RLS and prevent infinite recursion)
-- ============================================

-- Helper function to check if user is admin (runs as owner, bypassing RLS recursion)
CREATE OR REPLACE FUNCTION public.is_admin(user_id UUID)
RETURNS BOOLEAN AS $$
BEGIN
    RETURN EXISTS (
        SELECT 1 FROM public.users
        WHERE id = user_id AND role = 'admin'
    );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Helper function to promote user to admin
CREATE OR REPLACE FUNCTION public.make_admin(user_id UUID)
RETURNS VOID AS $$
BEGIN
    UPDATE public.users SET role = 'admin' WHERE id = user_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Helper function to check if user owns any course places
CREATE OR REPLACE FUNCTION public.is_owner(user_id UUID)
RETURNS BOOLEAN AS $$
BEGIN
    RETURN EXISTS (
        SELECT 1 FROM public.course_places
        WHERE owner_id = user_id
        LIMIT 1
    );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

